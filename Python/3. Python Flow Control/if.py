# Grade System

marks = 70

if marks>=80 and marks<=100:
    print('A+ Grade')
elif marks>=65 and marks<=79:
    print('A Grade')
elif marks>=55 and marks<=64:
    print('B Grade')
elif marks>=50 and marks<=54:
    print('C Grade')
else:
    print('Failed')
